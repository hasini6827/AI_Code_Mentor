# 8 AI Detection
def detect_ai_generated(code):

    prompt = f"""
Estimate the probability that the following Python code was generated by an AI.

Be conservative. Only give high probability if strongly confident.

Code:
{code}

Respond ONLY:
AI_PROBABILITY: X%
"""

    response = client.chat.completions.create(
        model=config.model_name,
        temperature=0,
        messages=[{"role": "user", "content": prompt}]
    )

    text = response.choices[0].message.content

    match = re.search(r'AI_PROBABILITY:\s*(\d+)%', text)
    gpt_prob = int(match.group(1)) if match else 50

    score = gpt_prob

    lines = code.strip().split("\n")

    if len(lines) <= 5:
        score -= 25

    if "input(" in code or "print(" in code:
        score -= 10

    if "#" in code:
        score -= 10

    if "  " in code:
        score -= 5

    if len(lines) > 15:
        score += 10

    if "temp" in code or "result" in code:
        score += 5

    
    final_score = max(0, min(100, score))

    if final_score >= 85:
        verdict = "AI"
    elif final_score >= 60:
        verdict = "UNCERTAIN"
    else:
        verdict = "HUMAN"

    return final_score, verdict

