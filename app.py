# 10 Review Route
@app.route("/review", methods=["POST"])
def review():

    if "user_id" not in session:
        return jsonify({"review": "Please login first."})

    code = request.form["code"]

    valid, message = validate_code(code)
    if not valid:
        return jsonify({"review": message})

    try:

        verdict, confidence = get_verdict(code)

        feedback = get_feedback(code, verdict)

        time_complexity = get_time_complexity(code) if verdict == "CORRECT" else None
        space_complexity = get_space_complexity(code) if verdict == "CORRECT" else None

        ai_probability, ai_verdict = detect_ai_generated(code)
        ai_flag = ai_probability >= 75

        cur = mysql.connection.cursor()

        error_count = 0 if verdict == "CORRECT" else 1

        cur.execute("""
        INSERT INTO submissions
        (user_id, goal, code, feedback, verdict, confidence, error_count, plagiarism_flag, plagiarism_probability)
        VALUES (%s,%s,%s,%s,%s,%s,%s,%s,%s)
        """,
        (
            session["user_id"],
            "N/A",
            code,
            feedback,
            verdict,
            confidence,
            error_count,
            ai_flag,
            ai_probability
        ))

        mysql.connection.commit()
        cur.close()

        log_event({
            "type": "review",
            "correctness": verdict,
            "confidence": confidence,
            "ai_probability": ai_probability
        })

        return jsonify({
            "feedback": feedback,
            "verdict": verdict,
            "confidence": confidence,
            "time_complexity": time_complexity,
            "space_complexity": space_complexity,
            "plagiarism_flag": ai_flag,
            "plagiarism_probability": ai_probability
        })

    except Exception as e:
        log_event({"type": "error", "error": str(e)})
        return jsonify({"review": "AI evaluation failed."})

