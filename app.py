@app.route("/profile")
def profile():

    if "user_id" not in session:
        return redirect("/login")

    user_id = session["user_id"]

    cur = mysql.connection.cursor()

    cur.execute("SELECT name FROM users WHERE id=%s", (user_id,))
    user = cur.fetchone()

    cur.execute("""
    SELECT 
    COUNT(*) AS total_submissions,
    SUM(error_count) AS total_errors,
    SUM(plagiarism_flag) AS plagiarism_count
    FROM submissions
    WHERE user_id=%s
    """, (user_id,))
    
    stats = cur.fetchone()

    cur.execute("""
    SELECT verdict, confidence, plagiarism_flag, plagiarism_probability
    FROM submissions
    WHERE user_id=%s
    ORDER BY id DESC
    LIMIT 10
    """, (user_id,))
    
    history = cur.fetchall()

    cur.execute("""
    SELECT 
    u.name,
    SUM(s.error_count) AS errors,
    SUM(s.plagiarism_flag) AS plagiarism
    FROM users u
    JOIN submissions s ON u.id = s.user_id
    GROUP BY u.id
    ORDER BY plagiarism ASC, errors ASC
    LIMIT 5
    """)

    leaderboard = cur.fetchall()

    cur.close()

    return render_template(
        "profile.html",
        name=user[0],
        submissions=stats[0] or 0,
        errors=stats[1] or 0,
        plagiarism=stats[2] or 0,
        history=history,
        leaderboard=leaderboard
    )

